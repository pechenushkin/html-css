<h1>Типы тегов</h1>

<p>Каждый HTML-тег имеет свой смысл и назначение.</p>

Часть тегов служебные и их никогда не видно на странице.</p>

Другие теги используются для форматирования текста и их влияние весьма заметно.</p>

Например, если обернуть текст в тег h1, то он станет крупнее и вокруг него появятся отступы, так как h1 — это заголовок.

